CREATE TABLE TRIP_PLACE_MAP (
    ID UUID DEFAULT "itinerary".uuid_generate_v4(),
    TRIP_PLACE_MAP_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    TRIP_ID UUID NOT NULL,
    PLACE_ID UUID NOT NULL,
    IS_ACTIVE BOOLEAN NOT NULL,
    CREATED_ON TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CREATED_BY VARCHAR(100) NOT NULL,
    UPDATED_ON TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_BY VARCHAR(100) NOT NULL,
    CONSTRAINT TRIP_PLACE_MAP_PKEY PRIMARY KEY (ID),
    CONSTRAINT TRIP_ID_FKEY FOREIGN KEY (TRIP_ID) REFERENCES itinerary.TRIP(ID),
    CONSTRAINT PLACE_ID_FKEY FOREIGN KEY (PLACE_ID) REFERENCES itinerary.PLACE(ID)
);

CREATE TABLE PLACE_ACTIVITY_MAP (
    ID UUID DEFAULT "itinerary".uuid_generate_v4(),
    PLACE_ACTIVITY_MAP_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    PLACE_ID UUID NOT NULL,
    ACTIVITY_ID UUID NOT NULL,
    IS_ACTIVE BOOLEAN NOT NULL,
    CREATED_ON TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CREATED_BY VARCHAR(100) NOT NULL,
    UPDATED_ON TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_BY VARCHAR(100) NOT NULL,
    CONSTRAINT PLACE_ACTIVITY_MAP_PKEY PRIMARY KEY (ID),
    CONSTRAINT PLACE_ID_FKEY FOREIGN KEY (PLACE_ID) REFERENCES itinerary.PLACE(ID),
    CONSTRAINT ACTIVITY_ID_FKEY FOREIGN KEY (ACTIVITY_ID) REFERENCES itinerary.ACTIVITY(ID)
);